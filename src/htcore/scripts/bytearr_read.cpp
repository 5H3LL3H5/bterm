unsigned int ByteArrRead::getU08(unsigned int index)
{
   unsigned int ret = 0x100;
   if (p_data->size() > index){
      ret = (*p_data)[index];
   }

   return ret;
}



std::shared_ptr<const std::vector<uint8_t>> ByteArrRead::getData() const
{
   return p_data;
}


