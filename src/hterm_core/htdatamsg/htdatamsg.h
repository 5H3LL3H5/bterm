#ifndef _HTDATAMSG_H
#define _HTDATAMSG_H

/*******************************************************************************
 * INCLUDED FILES
 ******************************************************************************/

#include "htdatapart.h"

#include <vector>
#include <string>

using namespace std;


/*******************************************************************************
 * CLASS DECLARATION
 ******************************************************************************/

class HTDataMsg
{
   /****************************************************************************
    * TYPES
    ***************************************************************************/

   /****************************************************************************
    * CONSTRUCTOR, DESTRUCTOR
    ***************************************************************************/
public:
   explicit HTDataMsg();


   /****************************************************************************
    * PRIVATE DATA
    ***************************************************************************/
private:

   vector<HTDataPart> data_parts;


   /****************************************************************************
    * STATIC METHODS
    ***************************************************************************/

   /****************************************************************************
    * METHODS
    ***************************************************************************/

public:
   void addData(HTDataPart::Type type, const vector<unsigned char> &data);
   void addData(HTDataPart::Type type, unsigned char byte);

   void clear();

   vector<unsigned char> getUserData() const;
   vector<unsigned char> getRawData() const;

   std::string toString() const;

   /****************************************************************************
    * SIGNALS, SLOTS
    ***************************************************************************/


   /*******************************************************************************
    * OPERATORS
    ******************************************************************************/

public:
   inline bool operator==(const HTDataMsg &other) const {
      return (this->data_parts == other.data_parts);
   }
};


#endif // _HTDATAMSG_H
